# Integers
print(1+1)
print(2-1)
print(2*4)
print(40/10)

# Strings
print("John")  # Constants
name = "RAY"  # Variables
print(name)

# String Methods
print(name.upper())  # UPPER
print(name.lower())  # lower
print(len(name))  # len - length
print(name.capitalize())  # Capitalizes the first character
print(name.count("r"))
print(name.count("R"))

# Escaping special Characters

# print('Hello, it's nice outside) #Syntax Error
print('Hello, it\'s nice outside')  # Syntax Error


# Variables

# Variable names must follow:
#       Can start with _ or letter
#       Can contain _ Letter or number

country = "Germany"
team = country
print(team)

#       Can reassign variable values
part1 = "Trevor"
part2 = "Mark"
part1 = "Bernard"

